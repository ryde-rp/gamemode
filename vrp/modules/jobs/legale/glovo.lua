function tvRP.pickGlovoRestaurant()
	local user_id = vRP.getUserId(source)
	math.randomseed(os.time() * user_id)

	local rData = table.rnd(deliveryItems)
	local theReward = rData.reward.min
	
	if theReward ~= rData.reward.max then
		theReward = math.random(theReward, rData.reward.max)
	end

	inDelivery[user_id] = {
		item = rData[3],
		reward = theReward,
	}

	return {
		pos = rData[1],
		delivery = table.rnd(deliveryPoints),
		item = rData[2],
		reward = theReward,
	}
end

function tvRP.pickupGlovoItem()
	local user_id = vRP.getUserId(source)
	local deliveryData = inDelivery[user_id]

	local function ban()
		vRP.banPlayer(0, user_id, -1, "Injection detected [vrp][glovoTask]")
	end

	if not deliveryData then
		return ban()
	else
		inDelivery[user_id].pickedUp = true
		
		if vRP.canCarryItem(user_id, deliveryData.item, 1, true) then
			vRP.giveInventoryItem(user_id, deliveryData.item, 1, true)
			vRPclient.notify(source, {"Ai ridicat pachetul, acum livreaza-l la adresa clientului!", "warning", false, "fas fa-location-dot"})
		end
	end
end

function tvRP.finishGlovoTask()
	local user_id = vRP.getUserId(source)
	local deliveryData = inDelivery[user_id]

	local function ban()
		vRP.banPlayer(0, user_id, -1, "Injection detected [vrp][glovoTask]")
	end

	local function finishTask()
		inDelivery[user_id] = nil

		if vRP.tryGetInventoryItem(user_id, deliveryData.item, 1, true) then
			vRP.giveMoney(user_id, deliveryData.reward)

			vRPclient.notify(source, {"ðŸ’¶ Ai castigat $"..deliveryData.reward, "info"})
		else
			vRPclient.notify(source, {"Ai pierdut comanda iar clientul nu te-a platit!", "error", false, "fas fa-location-dot"})
		end
	end

	if not deliveryData then
		return ban()
	elseif not deliveryData.pickedUp then
		return ban()
	elseif not uDeliveries[user_id] then
		uDeliveries[user_id] = os.time() + 5
		finishTask()
	elseif uDeliveries[user_id] > os.time() then
		uDeliveries[user_id] = nil
		return ban()
	else
		uDeliveries[user_id] = nil
		finishTask()
	end
end

